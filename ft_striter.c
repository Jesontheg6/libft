#include "libft.h"

void	ft_striter(char *s, void (*f) (char *))
{
	unsigned int i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			f(s + i);
			i++;
		}
		s[i] = '\0';
	}
}
