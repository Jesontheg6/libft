#include "libft.h"
#include "string.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	n;

	n = strlen(s1);
	i = 0;
	while (s2[i])
		s1[n++] = s2[i++];
	s1[n] = '\0';
	return (s1);
}
